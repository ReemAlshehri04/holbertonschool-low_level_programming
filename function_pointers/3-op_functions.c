#include "3-calc.h"
/**
	* op_add - addition
	* @a: first int
	* @b: second int
	* Return: sum
	*/
int op_add(int a, int b)
{
return (a + b);
}
/**
	* op_sub - subtraction
	* @a: first int
	* @b: second int
	* Return: difference
	*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
	* op_mul - multiplication
	* @a: first int
	* @b: second int
	* Return: product
	*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
	* op_div - division
	* @a: first int
	* @b: second int
	* Return: quotient
	*/
int op_div(int a, int b)
{
return (a / b);
}
/**
	* op_mod - modulo
	* @a: first int
	* @b: second int
	* Return: remainder
*/
int op_mod(int a, int b)
{
return (a % b);
}
